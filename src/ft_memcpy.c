#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = (unsigned char *)dst;
	s = (const unsigned char *)src;
	if (!d && !s && n)
		return (dst);
	if (!n)
		return (dst);
	while (n-- > 0)
		*d++ = *s++;
	return (dst);
}
